        private void slash(){
            String fName = "[slash]";
            String subcommandname=gSlashCommandEvent.getSubcommandName();
            String subcommandgroup=gSlashCommandEvent.getSubcommandGroup();
            if(subcommandgroup==null)subcommandgroup="";
            if(subcommandname==null)subcommandname="";
            List<OptionMapping>options=gSlashCommandEvent.getOptions();
            String optionValue_From="",optionValue_Tags="";boolean optionValue_NSFW=false;
            boolean optionProvided_From=false,optionProvided_Tags=false,optionProvided_NSFW=false;
            for(OptionMapping option:options){
                String name=option.getName();
                switch (name){
                    case icommandImageSearcher.SLASH_OPTIONS.from:optionValue_From=option.getAsString();optionProvided_From=true;break;
                    case icommandImageSearcher.SLASH_OPTIONS.tags:optionValue_Tags=option.getAsString();optionProvided_Tags=true;break;
                    case icommandImageSearcher.SLASH_OPTIONS.nsfw:optionValue_NSFW=option.getAsBoolean();optionProvided_NSFW=true;break;
                }
                if(optionValue_NSFW&&!gTextChannel.isNSFW()){
                    logger.warn(fName+"not nsfw channel>return");
                    lsMessageHelper.lsSendQuickErrorEmbedMessageResponse(gTextChannel,gTitle,"This is not a nsfw channel!");
                    return;
                }
                JSONObject jsonOptions=new JSONObject();
                lcE621 e621=new lcE621();
                lcFURAFFINITY fa=new lcFURAFFINITY(gGlobal);
                lcINKBUNNY inkbunny=new lcINKBUNNY(gGlobal);
                HttpResponse<JsonNode> responseJsonNode=null;
                HttpResponse<String> responseString=null;
                switch (optionValue_From){
                    case icommandImageSearcher.FROM.e621:
                        jsonOptions=e621Options(optionValue_Tags,optionValue_NSFW);
                        if(jsonOptions==null){
                            logger.warn(fName+"invalid tags json");
                            lsMessageHelper.lsSendQuickErrorEmbedMessageResponse(gTextChannel,gTitle,"Invalid tags!");
                            return;
                        }
                        responseJsonNode=e621.reqSearchGallery(jsonOptions);
                        break;
                    case icommandImageSearcher.FROM.fa:
                        responseString=fa.reqGetGallerySearch(jsonOptions);
                        break;
                    case icommandImageSearcher.FROM.inkbunny:
                        responseJsonNode=inkbunny.reqGallerySearch(jsonOptions);
                        break;
                    default:
                        logger.warn(fName+"invalid from");
                        lsMessageHelper.lsSendQuickErrorEmbedMessageResponse(gTextChannel,gTitle,"Invalid from option!");
                        return;
                }
                boolean invalidResponse=false;
                if(responseJsonNode==null&&responseString==null){
                    logger.warn(fName+"both responses are null");
                    invalidResponse=true;
                }
                else if(responseJsonNode!=null&&!responseJsonNode.isSuccess()){
                    logger.warn(fName+"responseJsonNode failed status");
                    invalidResponse=true;
                }
                else if(responseString!=null&&!responseString.isSuccess()){
                    logger.warn(fName+"responseString failed status");
                    invalidResponse=true;
                }
                if(invalidResponse){
                    logger.warn(fName+"invalidResponse");
                    lsMessageHelper.lsSendQuickErrorEmbedMessageResponse(gTextChannel,gTitle,"Error!");
                    return;
                }
                switch (optionValue_From){
                    case icommandImageSearcher.FROM.e621:
                        break;
                    case icommandImageSearcher.FROM.fa:
                        break;
                    case icommandImageSearcher.FROM.inkbunny:
                        break;
                }
            }
        }
        private JSONObject e621Options(String utags,boolean isNsfw) {
            String fName = "[e621Options]"; logger.info(fName); JSONObject response=new JSONObject();
            try{

                logger.info(fName+"utags="+utags);
                String rating="s";response.put("rating","s");
                if(isNsfw)response.put("rating","e");

                String tags = "";response.put("tags","");
                String[] items=utags.split("\\s+");
                for(String item:items){
                    if(!item.equalsIgnoreCase("explicit")&&!item.equalsIgnoreCase("safe")){
                        if(tags.isEmpty()){
                            tags= URLEncoder.encode(item, StandardCharsets.UTF_8.toString());
                        }else{
                            tags+="+"+URLEncoder.encode(item, StandardCharsets.UTF_8.toString());
                        }
                    }
                }
                response.put("tags",tags);
                if(tags.toLowerCase().contains("cub")||tags.toLowerCase().contains("kid")||tags.toLowerCase().contains("child")||tags.toLowerCase().contains("baby")){
                    logger.warn(fName+"contains ban words");response.put("error","ban words");return response;
                    //llQuickEmbedChannelResponse(gTextChannel,gTitle,"Contains blackwords!",llColorRed);
                }
                String limit="320";
                String option="?"; response.put("option","");
                option+="limit="+limit;
                response.put("limit",limit);
                if(!rating.isEmpty()||!tags.isEmpty()){
                    if(!tags.isEmpty()&&!rating.isEmpty()){
                        option+="&tags="+tags+"+rating:"+rating;
                    }else
                    if(!tags.isEmpty()){
                        option+="&tags="+tags;
                    }else
                    if(!rating.isEmpty()){
                        option+="&rating:"+rating;
                    }
                }
                response.put("option",option);
                logger.info(fName+"option="+option);
                response.put("done",true);
                return response;
            } catch (Exception e) {
                logger.error(fName+"exception:"+e);
                return null;
            }

        }


